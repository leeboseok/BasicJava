package lng;

public class LONG {
	public static void main(String[] args) {
		//long : 롱
		//아주 큰 수를 저장할 수 있는 규격
		//기본 자료형중 가장 큰 수를 저장할 수 있는 규격
		//-9223372036854775808 ~ 9223372036854775807
		
		//int의 범위를 벗어나는 크기의 수는 뒤에 l, L 붙여서 표기
		long a= 21000000000L;
		System.out.println(a);
	}//main end
}//class end
