package test;

public class Sample01 {
	public static void main(String[] args) {
		System.out.println("동해물과 백두산이 \n마르고 닳도록");
		// \n : enter key
		// \t : tap key //적당히(윗쪽에 출력된 내용에 맞춰서..)
		System.out.println("이름:이보석 나이:21 주소:서울시 성동구");
		System.out.println("이름:안현 나이:20 주소:청주시 상당구");
		System.out.println("이름:이보석 \t 나이:21 \t 주소:서울시 성동구");
		System.out.println("이름:안현 \t 나이:20 \t 주소:청주시 상당구");
		
		System.out.println("문자 하나만을 적을 때는 \'안에 넣어야 한다");
		System.out.println("명령어에 쓰이는 문자를 그냥 출력시킬때는 앞에 \\를 붙인다");
		
	}//main end
}//class end
